#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const svgPath = path.join(__dirname, '../public/our-community/community-map.svg');
const svgContent = fs.readFileSync(svgPath, 'utf-8');

// Region detection logic (Mexico as separate region)
function getRegion(x, y) {
  if (x >= 800 && y >= 250) return 'OCEANIA';
  if (x >= 500 && x < 600 && y < 100) return 'EUROPE';
  if (x < 500 && y < 100) return 'NORTH_AMERICA';
  if (x < 450 && y < 150) return 'NORTH_AMERICA'; // USA & Canada only
  if (x < 320 && y >= 150 && y < 200) return 'MEXICO'; // Mexico separate region
  if (x < 320 && y >= 200 && y < 240) return 'CENTRAL_AMERICA'; // Central America below Mexico
  if (x < 360 && y >= 240) return 'SOUTH_AMERICA';
  if (x >= 420 && x < 480 && y >= 200 && y < 260) return 'AFRICA';
  if (x >= 450 && x < 700 && y >= 100 && y < 140) return 'EUROPE';
  if (x >= 450 && x < 620 && y >= 100 && y < 175) return 'EUROPE';
  if (x >= 580 && x < 720 && y >= 140 && y < 250) return 'MIDDLE_EAST';
  if (x >= 450 && x < 700 && y >= 165 && y < 420) return 'AFRICA';
  if (x >= 600 && y < 100) return 'ASIA';
  if (x >= 600 && y < 290) return 'ASIA';
  if (x >= 750 && y >= 200 && y < 320) return 'ASIA';
  return 'UNKNOWN';
}

// Parse all polygons and classify them by region
const polygonRegex = /<polygon points="([^"]*)" fill="([^"]*)"\s*\/>/g;
const regionPolygons = {
  NORTH_AMERICA: [],
  MEXICO: [],
  CENTRAL_AMERICA: [],
  SOUTH_AMERICA: [],
  EUROPE: [],
  AFRICA: [],
  MIDDLE_EAST: [],
  ASIA: [],
  OCEANIA: [],
};

let polygonIndex = 0;
let match;

while ((match = polygonRegex.exec(svgContent)) !== null) {
  const pointsStr = match[1];

  // Get first point to determine region
  const firstPoint = pointsStr.split(' ')[0];
  const [x, y] = firstPoint.split(',').map(Number);

  // Skip stray polygons
  if (x < 50 && y > 150 && y < 350) {
    continue;
  }

  const region = getRegion(x, y);

  if (regionPolygons[region]) {
    regionPolygons[region].push({
      index: polygonIndex,
      points: pointsStr,
      x,
      y,
    });
  }

  polygonIndex++;
}

// Generate TypeScript mapping file
const tsContent = `/**
 * AUTO-GENERATED: Mapping of SVG polygon indices to geographic regions
 * Generated by analyze-svg-polygons.js
 *
 * Each region contains an array of polygon indices from the base SVG.
 * To "activate" a user marker, select a random polygon from the region
 * and overlay it with a different color.
 */

export const REGION_POLYGON_INDICES: Record<string, number[]> = {
  NORTH_AMERICA: [${regionPolygons.NORTH_AMERICA.map(p => p.index).join(', ')}],
  CENTRAL_AMERICA: [${regionPolygons.CENTRAL_AMERICA.map(p => p.index).join(', ')}],
  SOUTH_AMERICA: [${regionPolygons.SOUTH_AMERICA.map(p => p.index).join(', ')}],
  EUROPE: [${regionPolygons.EUROPE.map(p => p.index).join(', ')}],
  AFRICA: [${regionPolygons.AFRICA.map(p => p.index).join(', ')}],
  MIDDLE_EAST: [${regionPolygons.MIDDLE_EAST.map(p => p.index).join(', ')}],
  ASIA: [${regionPolygons.ASIA.map(p => p.index).join(', ')}],
  OCEANIA: [${regionPolygons.OCEANIA.map(p => p.index).join(', ')}],
};

export const POLYGON_DATA: Array<{ index: number; points: string; x: number; y: number; region: string }> = [
${Object.entries(regionPolygons).map(([region, polygons]) =>
  polygons.map(p => `  { index: ${p.index}, points: "${p.points}", x: ${p.x}, y: ${p.y}, region: "${region}" }`).join(',\n')
).join(',\n')}
];

/**
 * Get a random polygon index from a region
 */
export function getRandomPolygonFromRegion(region: string): number {
  const indices = REGION_POLYGON_INDICES[region];
  if (!indices || indices.length === 0) return -1;
  return indices[Math.floor(Math.random() * indices.length)];
}

/**
 * Get polygon data by index
 */
export function getPolygonData(index: number) {
  return POLYGON_DATA.find(p => p.index === index);
}
`;

// Write output
const outputPath = path.join(__dirname, '../src/components/community-map/svg-polygon-mapping.ts');
fs.writeFileSync(outputPath, tsContent);

// Print summary
console.log('\n=== SVG Polygon Analysis ===\n');
Object.entries(regionPolygons).forEach(([region, polygons]) => {
  console.log(`${region}: ${polygons.length} polygons`);
});
console.log(`\nTotal: ${polygonIndex} polygons analyzed`);
console.log(`\nMapping saved to: ${outputPath}`);
